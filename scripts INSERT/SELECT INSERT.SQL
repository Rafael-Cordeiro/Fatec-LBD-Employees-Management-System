
-- RETORNA ID DE TURNO --
(SELECT TUR_ID 
FROM TURNOS 
WHERE (TUR_HORA_ENTRADA = to_date('01/01/2021 08:00:00', 'DD/MM/YYYY HH24:MI:SS') 
        AND TUR_SAB = 'N'));



(SELECT TUR_ID 
FROM TURNOS 
WHERE (TUR_HORA_SAIDA = to_date('01/01/2021 15:00:00', 'DD/MM/YYYY HH24:MI:SS') 
        AND TUR_SAB = 'N'));


